        title_sz = OxmlElement('w:sz')
        title_sz.set(qn('w:val'), '24')
        title_rpr.append(title_sz)
        title_r.append(title_rpr)
        title_t = OxmlElement('w:t')
        title_t.text = title
        title_r.append(title_t)
        title_p.append(title_r)

